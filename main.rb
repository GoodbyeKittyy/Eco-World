def calculate_statistics(numbers)
  return nil if numbers.nil? || numbers.empty?

  count  = numbers.size
  total  = numbers.sum
  mean   = total.to_f / count

  sorted = numbers.sort
  median = if count.odd?
             sorted[count / 2].to_f
           else
             (sorted[count / 2 - 1] + sorted[count / 2]) / 2.0
           end

  variance = numbers.sum { |n| (n - mean)**2 } / count.to_f
  std_dev  = Math.sqrt(variance)

  {
    count:   count,
    mean:    mean,
    median:  median,
    std_dev: std_dev,
    min:     sorted.first,
    max:     sorted.last,
  }
end

